def read_file(filename):
    with open(filename, 'r') as file:
        return [line.strip('\n') for line in file]


def find_code(geometry, paths):
    pos = '5'
    code = ""
    for path in paths:
        for direc in path:
            pos = geometry.get((pos, direc), pos)
        code += pos
    return code


